import numpy as np

# Declaración de matrices
A = np.array([[2, 4], [8, 7]])
print(f"A = {A}")

B = np.array([[0, -1], [-1, 0]])
print(f"B = {B}")

C = np.array([[4, 7, 6], [1, 9, 6], [3, 2, 3]])
print(f"C = {C}")

# Operaciones con matrices
D = A + B
print(f"D = {D}")
